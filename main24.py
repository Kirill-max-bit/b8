# Вычисляем средний балл для каждой группы
average_scores = [
    sum(sum(student) for student in group) / (len(group) * len(group[0]))
    for group in grades
]

# Находим номер лучшей группы
best_group = average_scores.index(max(average_scores)) + 1

print("Лучшая группа по среднему баллу:", best_group)
