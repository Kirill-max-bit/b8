# Количество учеников в классах (11 параллелей, 4 класса: А, Б, В, Г)
classes = [
    [23, 25, 27, 22],  # 1-я параллель
    [24, 26, 25, 23],
    [25, 27, 26, 24],
    [26, 28, 27, 25],
    [27, 29, 28, 26],
    [28, 30, 29, 27],
    [29, 31, 30, 28],
    [30, 32, 31, 29],
    [31, 33, 32, 30],
    [32, 34, 33, 31],
    [20, 25, 21, 26]   # 11-я параллель
]


# а) Минимальный класс в каждой параллели
min_in_parallel = [min(parallel) for parallel in classes]


min_in_class = [min(class_) for class_ in zip(*classes)]

print("а) Минимальные классы в каждой параллели:", min_in_parallel)
print("б) Минимальные классы среди А, Б, В, Г:", min_in_class)
