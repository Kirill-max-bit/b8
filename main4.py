for i in range(5):  # Цикл от 0 до 4
    for j in range(i, -1, -1):  # Внутренний цикл для чисел
        print(j, end=" ")
    print()


for i in range(6, 2, -1):  # Цикл от 6 до 2
    for j in range(i, 1, -1):  # Внутренний цикл для чисел
        print(j, end=" ")
    print()


for i in range(5):  # Цикл от 0 до 4
    for j in range(30 - i, 31):  # Внутренний цикл для чисел
        print(j, end=" ")
    print()


